package MisClases;

public class TV {
    private int canal;
    private int volumen;
    private boolean power;

    public int getCanal() {
        return canal;
    }

    public void setCanal(int x) {
        this.canal = x;
    }

    public int getVolumen() {
        return volumen;
    }

    public void setVolumen(int y) {
        this.volumen = y;
    }

    public boolean isPower() {
        return power;
    }

    public void setPower(boolean z) {
        this.power = z;
    }
    
}
